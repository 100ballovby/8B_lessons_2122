# строчки - упорядоченные последовательности
# строки имеют индексы. Индексы начинаются с 0
z = 'Рома тунеядец!'
print(z[5])  # т
print(z[-14])  # Р
# print(z[32]) -> IndexError
name = z[:4]  # z[0:4]
prof = z[5:]  # z[5:14]
print(name, prof)

