"""Функция range() - генератор арифметической последовательности"""

# range(5) - 0...4
# range(10, 15) - 10...14
# range(1, 12, 2) - 1, 3, 5, 7, 9, 11
x = 10
while x > 0:   # объявление
    print(x)  # тело цикла
    x -= 1  # тело цикла

# перепишем while через for
for x in range(10, 0, -1):
    print(x)
